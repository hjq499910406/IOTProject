let currentAppLocale = 'zh-CN';

export const appLocaleMessages = {
  'zh-CN': appLocaleZhCN,
  'en-US': appLocaleEnUS,
  'zh-TW': appLocaleZhHK,
  'zh-HK': appLocaleZhHK
};

const isObject = (val) => val && typeof val === 'object' && !Array.isArray(val);

const deepMerge = (target, source) => {
  const base = isObject(target) ? { ...target } : {};
  Object.keys(source || {}).forEach((key) => {
    const sourceVal = source[ key ];
    const targetVal = base[ key ];
    if (isObject(sourceVal) && isObject(targetVal)) {
      base[ key ] = deepMerge(targetVal, sourceVal);
      return;
    }
    base[ key ] = sourceVal;
  });
  return base;
};

export const getRuntimeLocale = () => {
  let tLocale = TFF?.common?.storage?.Cookie?.read?.('t_locale') || TFF?.common?.storage?.Cookie?.read?.('teld_locale') || TFF?.common?.storage?.Cookie?.read?.('wrp_locale');
  if (!tLocale && typeof navigator !== 'undefined' && navigator.language) {
    tLocale = navigator.language;
  }
  if (!tLocale || tLocale == 'zh') {
    tLocale = 'zh-CN';
  }
  return String(tLocale);
};

const getLocalePack = (locale) => {
  const localeKey = String(locale || getRuntimeLocale());
  return appLocaleMessages[ localeKey ] || appLocaleMessages[ 'zh-CN' ];
};

export const pageText = (key, fallback = '') => {
  if (!key) return fallback;
  const pack = getLocalePack(getRuntimeLocale());
  const value = String(key).split('.').reduce((current, part) => current?.[ part ], pack);
  return value == null || value === key ? (fallback || key) : value;
};

export const pageT = (key, fallback = '') => pageText(key, fallback);

export const injectAppLocaleMessages = (locale) => {
  const localeKey = String(locale || getRuntimeLocale());
  currentAppLocale = appLocaleMessages[ localeKey ] ? localeKey : 'zh-CN';
  const pack = getLocalePack(localeKey);
  window.localeMessage = deepMerge(window.localeMessage || {}, pack);
  return currentAppLocale;
};

export const applyAppI18n = (mergeLocaleMessage) => {
  const localeKey = injectAppLocaleMessages();
  const pack = getLocalePack(localeKey);
  if (document.body) {
    document.body.setAttribute('lang', getRuntimeLocale());
  }
  if (typeof mergeLocaleMessage === 'function') {
    mergeLocaleMessage(localeKey, pack);
  }
  return localeKey;
};

export const useAppI18n = () => {
  const VueI18n = window.VueI18n;
  const composer = VueI18n?.useI18n?.();
  applyAppI18n(composer?.mergeLocaleMessage);
  const tt = (key, fallback = '') => {
    const value = composer?.t?.(key);
    if (value && value !== key) return value;
    return pageText(key, fallback);
  };
  return { ...(composer || {}), tt };
};
